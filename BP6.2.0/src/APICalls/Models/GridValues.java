package APICalls.Models;

public class GridValues {

    private int GX;
    private int GY;

    public GridValues(int GX, int GY) {
        this.GX = GX;
        this.GY = GY;
    }

    public int getGX() {
        return GX;
    }

    public void setGX(int GX) {
        this.GX = GX;
    }

    public int getGY() {
        return GY;
    }

    public void setGY(int GY) {
        this.GY = GY;
    }
}
