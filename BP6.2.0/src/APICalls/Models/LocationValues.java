package APICalls.Models;

public class LocationValues {

    int RX;
    int RY;

    public LocationValues(int RX, int RY) {
        this.RX = RX;
        this.RY = RY;
    }

    public int getRX() {
        return RX;
    }

    public void setRX(int RX) {
        this.RX = RX;
    }

    public int getRY() {
        return RY;
    }

    public void setRY(int RY) {
        this.RY = RY;
    }
}
